                         <h3>VA Home Loans and Housing-Related Assistance</h3>

                         <p>
                            VA home loan programs may be used to obtain homes, condominiums, or manufactured homes; refinance an existing home loan; or install energy-saving
                            improvements. VA offers these three main types of guaranteed home loan benefits:
                        </p>
                        <ul class="bullet-lists">
                            <li>
                                <p>
                                <a href="http://benefits.va.gov/HOMELOANS/purchasecashout.asp">Purchase Loans</a>
                                </p>
                            </li>
                            <li>
                                <p>
                                Cash-Out Refinance Loans
                                </p>
                            </li>
                            <li>
                                <p>
                                <a href="http://benefits.va.gov/HOMELOANS/nadl.asp">Interest</a> Rate Reduction Refinance Loans
                                </p>
                            </li>
                        </ul>
                        <h5 class="matt-added" >Native American Veterans</h5>
                        <p>
                            For Native American Veterans who want to reside on Federal Trust land, VA’s Native American Direct Loan (NADL) program is another option. It provides
                        direct loans to eligible Native American Veterans for the purchase, construction, or improvement of a home.    <a href="http://www.benefits.va.gov/HOMELOANS/nadl.asp">Learn more about NADL</a>.
                        </p>
                        <h5 class="matt-added" >Adapted Homes for Disabled Veterans</h5>
                        <p>
                            VA also offers Veterans with certain service-connected disabilities grants to build an adapted home or install ramps, widen doors, or make other
                            modifications to live more independently. VA operates three types of grants that accommodate Veterans’ unique circumstances: Specially Adapted Housing,
                            Special Housing Adaptation, and Temporary Residence Adaptation.
                        </p>
                        <h5 class="matt-added" >Help for Homeless Veterans</h5>
                        <p>
                       <b> If you are homeless, at imminent risk of becoming homeless, contact your local VA medical center; call 877-4AID-VET (877-424-3838); or visit    <a href="http://www.va.gov/homeless">va.gov/homeless</a>.</b> VA can connect you with the care you need to get back on your feet.
                        </p>

                         <h5 class="matt-added" >How Loans are Underwriten</h5>
                        <p>
                            Private-sector lenders underwrite and fund VA home loans according to established VA standards. VA’s partial guaranty for these loans means that nearly 90
                            percent of all VA-guaranteed home loans are made with no down payment required.
                        </p>
                        <h5 class="matt-added">Delinquency</h5>
                        <p>
                            If a VA-guaranteed loan becomes delinquent, VA works with the borrower to avoid foreclosure, including providing financial counseling and, in some cases,
                            direct intervention with a mortgage loan servicer on the borrower’s behalf. In 2013, VA loans had the lowest foreclosure rate of all types of loans in the
                            market. If you are a Veteran or Servicemember having difficulty making mortgage payments, call 877-827-3702 to speak with a VA Loan Technician. More
                            information about avoiding foreclosure is at http://benefits.va.gov/HOMELOANS/resources_payments.asp.
                        </p>
                        <h5 class="matt-added">Borrowers</h5>
                        <p>
                            Borrowers must have suitable credit, sufficient income, and a valid Certificate of Eligibility (COE) to be eligible for a VA-guaranteed home loan. ocument
